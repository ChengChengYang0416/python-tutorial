#-------------- 1. operations in numbers --------------#
# division
divi_1 = 7/6
print(divi_1)
divi_2 = 7//6
print(divi_2)

# power
power = 3**4
print(power)

# modulo
modu = 13%3
print(modu)
#-------------- end of operations in numbers --------------#


#-------------- 2. operations in strings --------------#
